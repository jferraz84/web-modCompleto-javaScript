// function declaration
console.log(soma(5, 8))

function soma(x, y) {
    return x + y
}

// function expression 
const sub = function(x, y){
    return x - y
}
console.log(sub(5, 8))

// named function expression
const mult = function mult(x, y){
    return x * y
}
console.log(mult(5, 8))

